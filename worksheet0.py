"""8. Nested List Average:
Write a Python function that calculates the average of all elements in a nested list.
‚Ä¢ Requirements:‚Äì Define a function average
of all the elements.
nested(lst) that takes a nested list lst and returns the average‚Äì Example: For the input [[1, 2], [3, 4], [5, 6]], the output should be 3.5
"""

def average_nested(lst):
    def helper(sublist):
        total = 0
        count = 0
        for item in sublist:
            if isinstance(item, list):
                t, c = helper(item)  # recursively get sum and count
                total += t
                count += c
            else:
                total += item
                count += 1
        return total, count

    total_sum, total_count = helper(lst)
    if total_count == 0:
        return 0  # Avoid division by zero
    return total_sum / total_count

nested = [[1, 2], [3, 4], [5, 6]]

result = average_nested(nested)

